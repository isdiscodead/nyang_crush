package com.pro.nyangcrush;

public class NyangPosition {

    private int x;
    private int y;

    public NyangPosition(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

} // NyangPosition()
